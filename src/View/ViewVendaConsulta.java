    private void btn_PDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PDFActionPerformed
         if (venda == null) {
            JOptionPane.showMessageDialog(this, "Nenhuma venda selecionada!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Cria o JFileChooser
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Salvar Relat√≥rio como PDF");

        // Define o filtro para exibir apenas arquivos PDF
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos PDF (*.pdf)", "pdf");
        fileChooser.setFileFilter(filter);

        // Sugere um nome padr√£o para o arquivo
        String nomePadrao = "relatorio_venda_" + venda.getId() + ".pdf";
        fileChooser.setSelectedFile(new java.io.File(nomePadrao));

        // Exibe o di√°logo para salvar o arquivo
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            // Obt√©m o caminho selecionado pelo usu√°rio
            java.io.File arquivoSelecionado = fileChooser.getSelectedFile();

            // Garante que o arquivo tenha a extens√£o .pdf
            String caminhoArquivo = arquivoSelecionado.getAbsolutePath();
            if (!caminhoArquivo.toLowerCase().endsWith(".pdf")) {
                caminhoArquivo += ".pdf";
            }

            // Gera o relat√≥rio
            RelatorioPDF.gerarRelatorioVenda(venda, caminhoArquivo);

            // Exibe mensagem de sucesso
            JOptionPane.showMessageDialog(this, "Relat√≥rio gerado com sucesso!\nCaminho: " + caminhoArquivo, "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_PDFActionPerformed

