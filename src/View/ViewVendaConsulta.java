    private void btn_BuscarVendaPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarVendaPorIdActionPerformed
        limparCampos();
        String id = JOptionPane.showInputDialog(this, "Digite o c√≥digo da venda:");
        if (id == null || id.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Voc√™ deve informar um c√≥digo v√°lido!", "Valida√ß√£o de dados!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!id.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "O c√≥digo deve conter apenas n√∫meros!", "Valida√ß√£o de dados!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            //vendedor = new Vendedor();
            //cliente = new Cliente();
            venda = vendaDAO.BuscarVendaPorId(id);
            if (venda == null) {
                JOptionPane.showMessageDialog(this, "C√≥digo de venda n√£o encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                // Preencher os campos corretamente
                txt_idVenda.setText(String.valueOf(venda.getId()));
                txt_idCLiente.setText(String.valueOf(venda.getCliente().getIdCliente()));
                txt_idVendedor.setText(String.valueOf(venda.getVendedor().getId()));
                txt_cliente.setText(venda.getCliente().getCliNome());
                txt_vendedor.setText(venda.getVendedor().getNome());
                label_TotVenda.setText(String.valueOf(venda.getTotalVenda()));

                // Chamar a atualiza√ß√£o da tabela
                atualizaTabelaItens();
            }
        } catch (Exception e) {
            Logger.getLogger(ViewProdutoConsulta.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this, "Erro ao buscar venda: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_BuscarVendaPorIdActionPerformed

